// Check collision with walls
export const checkCollision = (position: Position, gameMap: GameMap): boolean => {
  // Convert position to grid coordinates
  const gridX = Math.floor(position.x / CELL_SIZE);
  const gridY = Math.floor(position.y / CELL_SIZE);
  
  // Check if position is within bounds
  if (gridX < 0 || gridX >= gameMap[0].length || gridY < 0 || gridY >= gameMap.length) {
    return true; // Out of bounds is a collision
  }
  
  // Check if there's a wall at this position
  return gameMap[gridY][gridX] === MAP_SYMBOLS.WALL;
};

